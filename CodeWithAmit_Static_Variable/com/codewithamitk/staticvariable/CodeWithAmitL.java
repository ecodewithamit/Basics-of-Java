package com.codewithamitk.staticvariable;

class CodeWithAmitL {
	static int i = 10;

	public static void main(String[] args) {
		System.out.println(i);
		System.out.println(CodeWithAmitL.i);
	}
}
// We can access static variable using class name also.
// Using class name within the same class is not recommended.
