package com.codewithamitk.staticvariable;

public class CodeWithAmitC {
	static int i;// declaration
	static int j = 10;// j is called initializer

	public static void main(String[] args) {
		System.out.println(i);
		System.out.println(j);
	}
}

/*
initializer = if the global variable gets  a value at the time of declaration 
	then it is called initializer
*/
