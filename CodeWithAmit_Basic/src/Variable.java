public class Variable {
    public static void main(String[] args) {
        
        // String variables
        String studentName = "codewithamit";             
        String userEmail = "codewithamit@gmail.com";    
        String cityName = "Patna";                 
        
        // int variables
        int totalMarks = 450;                      
        int numberOfBooks = 12;                    
        
        // boolean variable
        boolean isLoggedIn = true;                 
        
        // double/float variables
        double averageScore = 89.5;                
        float priceAfterDiscount = 299.99f;       
        
        // long variable
        long bankBalance = 1500000L;              
        
        // short variable
        short maxHeight = 180;
       
        
		
        System.out.println(studentName);
        System.out.println(totalMarks);
        System.out.println(isLoggedIn);
        System.out.println(averageScore);
        System.out.println(maxHeight);
        System.out.println(bankBalance);
        System.out.println(userEmail);
        System.out.println(numberOfBooks);
        System.out.println(priceAfterDiscount);
        System.out.println(cityName);
		
		
		//Printing with message
        System.out.println("Student Name: " + studentName);
        System.out.println("Total Marks: " + totalMarks);
        System.out.println("Is Logged In: " + isLoggedIn);
        System.out.println("Average Score: " + averageScore);
        System.out.println("Max Height: " + maxHeight);
        System.out.println("Bank Balance: " + bankBalance);
        System.out.println("User Email: " + userEmail);
        System.out.println("Number of Books: " + numberOfBooks);
        System.out.println("Price After Discount: " + priceAfterDiscount);
        System.out.println("City Name: " + cityName);
    }
}
